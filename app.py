def Spiral(q, r0, x, y, origin=(0.8, - 0.8), clockwise=False):
    """ Return a spiral field vector with origin at `origin`
    """
    x, y = x - origin[0], y - origin[1]
    if clockwise:
        return x - y, x + y
    else:
        return y - x, -x - y

